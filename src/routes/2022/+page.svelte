      <h1>Infi-Con: 20 oktober 2022</h1>

      <p>
        Nadat we in 2021 onverhoopt Infi-Con moesten annuleren als gevolg van COVID-19 zijn we dit jaar op donderdag 20 oktober terug met
        een nieuwe editie (in Utrecht). Het thema is “boeken” en we zijn ontzettend enthousiast dat Felienne Hermans als keynote spreker
        iets komt vertellen over haar boek “The Programmer's Brain”.
      </p>
      <p>
        Daarnaast zullen drie Infi-ers hun favoriete tech boek als inspiratiebron gebruiken voor hun talks. Omdat wij het afgelopen jaren
        enorm gemist hebben om face-to-face over ons vakgebied te praten, zijn er deze editie extra momenten om met elkaar te socializen.
        Inloop is vanaf 12:30 uur, talks starten om 13:00 uur en rond 17:00 uur sluiten we af met een borrel.
      </p>
